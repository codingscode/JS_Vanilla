/*
string.trim()

The trim() method removes whitespace from both sides of a string.
Note: The trim() method does not change the original string.

*/

var uma_string1 = '       primeira string        '
var uma_string2 = '       segunda string'
var uma_string3 = 'terceira string            '

console.log(uma_string1.trim())
console.log(uma_string2.trim())
console.log(uma_string3.trim())
