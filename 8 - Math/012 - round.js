/*
   The round() method rounds a number to the nearest integer.

Note: 2.49 will be rounded down (2), and 2.5 will be rounded up (3).
*/

var arredondar1 = Math.round(6.2)
var arredondar2 = Math.round(6.49)
var arredondar3 = Math.round(6.5)
var arredondar4 = Math.round(6.8)
var arredondar5 = Math.round(1.7)

console.log(`${arredondar1} || ${arredondar2} || ${arredondar3} || ${arredondar4} || ${arredondar5}`)
