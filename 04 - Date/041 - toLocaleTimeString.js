/*
Date.toLocaleTimeString()

The toLocaleTimeString() method returns the time portion of a Date object as a string, using locale conventions.

*/

var uma_data = new Date()
console.log(uma_data)
console.log(typeof uma_data)
console.log('-------------------')

var n = uma_data.toLocaleTimeString()
console.log(n)
console.log(typeof n)
