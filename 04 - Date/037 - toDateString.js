/*
Date.toDateString()

The toDateString() method converts the date (not the time) of a Date object into a readable string.

*/

var uma_data = new Date()
console.log(uma_data) 
console.log(typeof uma_data) 
console.log('------------------')

var n = uma_data.toDateString()
console.log(n)
console.log(typeof n)
